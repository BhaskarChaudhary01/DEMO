public class AreaOfShapes {
    public int area(int l,int b){
        return (l*b);
    }
    public int area(int l){
        return (l*l);
    }
    public double area(float rad){
        return (3.14*rad*rad);
    }
    public double area(float base,float height){
        return (0.5*base*height);
    }
}
